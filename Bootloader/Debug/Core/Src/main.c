void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	int length;
	if (huart->Instance == USART1) {
		memcpy(&length, &head[8], 4);
		page_buf = (u8 *)calloc(length + 2, sizeof(u8));
		if (!page_buf) {
			HAL_UART_Transmit(&huart1, (u8 *) "cannot allocate memory\n", strlen("cannot allocate memory\n"), 100);
			return;
		}
		HAL_UART_Receive(&huart1, page_buf, (length + 2), 15000);
		handle_request(&dev, head, page_buf);
		free(page_buf);
	}
	memset(head, 0, sizeof(head));
	HAL_UART_Receive_IT(&huart1, head, sizeof(head));
}
